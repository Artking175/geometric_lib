
def area(a):
    '''
    возвращает площаь квадрата

    возводит сторону (a) в квадрат

    Пример:
    int a = 7

    возвращаемое значение: 49
    '''
    return a * a


def perimeter(a):
    '''
    возвращает периметр квадрата

    умножает сторону (a) на 4

    Пример:
    int a = 7

    возвращаемое значение: 28
    '''
    return 4 * a
