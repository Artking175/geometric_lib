def area(a, b):
    '''
    возвращает площаь прямоугольника

    умножает сторону (a) на сторону (b)

    Пример:
    int a = 2
    int b = 3

    возвращаемое значение: 6
    '''
    return a * b


def perimeter(a, b):
    '''
    возвращает периметр прямоугольника

    суммирует сторону (a) и сторону (b) и умножает на 2

    Пример:
    int a = 2
    int b = 3

    возвращаемое значение: 10
    '''
    return (a + b)*2
